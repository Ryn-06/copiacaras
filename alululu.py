# Cargar modelo LBPH
try:
    recognizer = cv2.face.LBPHFaceRecognizer_create(
        radius=1,
        neighbors=8,
        grid_x=8,
        grid_y=8,
        threshold=100  # â† UMORAL PERMISIVO
    )
    recognizer.read('modelo_lbph.xml')
    st.sidebar.success("âœ… LBPH cargado")
except Exception as e:
    st.error(f"âŒ Error al cargar modelo: {str(e)}")
    st.stop()

# Cargar etiquetas
label_map = np.load('etiquetas_personas.npy', allow_pickle=True).item()

# Cargar detector
detector = cv2.CascadeClassifier(
    cv2.data.haarcascades + 'haarcascade_frontalface_default.xml'
)

