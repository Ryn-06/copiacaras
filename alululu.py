# Captura
img_file = st.camera_input("Tomar foto")

if img_file:
    with st.spinner("ğŸ” Analizando rostro..."):
        # Convertir a OpenCV
        bytes_data = img_file.getvalue()
        cv2_img = cv2.imdecode(np.frombuffer(bytes_data, np.uint8), cv2.IMREAD_COLOR)
        gray = cv2.cvtColor(cv2_img, cv2.COLOR_BGR2GRAY)

        # Detectar rostros
        faces = detector.detectMultiScale(
            gray, scaleFactor=1.1, minNeighbors=5, minSize=(30, 30)
        )

        if len(faces) == 0:
            st.error("âŒ No se detectÃ³ ningÃºn rostro")
            st.stop()

        # Procesar rostro mÃ¡s grande
        faces = sorted(faces, key=lambda f: f[2] * f[3], reverse=True)
        (x, y, w, h) = faces[0]
        rostro = gray[y:y+h, x:x+w]

        # ğŸ”‘ PREPROCESAMIENTO IGUAL AL ENTRENAMIENTO
        rostro = cv2.resize(rostro, (150, 150), interpolation=cv2.INTER_AREA)
        rostro = cv2.equalizeHist(rostro)

        # Predecir
        id_predicho, distancia = recognizer.predict(rostro)
        confianza = max(0, 100 - distancia)
        timestamp = datetime.now().strftime("%H:%M:%S")

        # Mostrar resultado
        st.divider()

        # ğŸ”‘ UMBRAL CORREGIDO: distancia < 100 (mÃ¡s permisivo)
        if id_predicho in label_map and distancia < 100:
            nombre = label_map[id_predicho]

            if confianza >= 70:
                st.success(f"âœ… {nombre} reconocido")
                st.write(f"**Confianza:** {confianza:.0f}%")
                st.write(f"**Distancia:** {distancia:.1f}")
                st.write(f"**Hora:** {timestamp}")

            elif confianza >= 40:
                st.warning(f"âš ï¸ {nombre} (baja confianza)")
                st.write(f"**Confianza:** {confianza:.0f}%")
                st.write(f"**Distancia:** {distancia:.1f}")

            else:
                st.info(f"â“ Posible {nombre} (confianza muy baja)")
                st.write(f"**Confianza:** {confianza:.0f}%")
                st.write(f"**Distancia:** {distancia:.1f}")
        else:
            st.error("âŒ No reconocido")
            st.write(f"**Distancia:** {distancia:.1f}")
            st.write("El rostro no coincide con personas registradas")

        st.divider()

